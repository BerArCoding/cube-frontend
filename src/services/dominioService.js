import { authAPI } from './auth';

const API_BASE_URL = import.meta.env.VITE_API_BASE_URL || 'http://localhost:3001';

class DominioService {
  constructor() {
    this.baseUrl = `${API_BASE_URL}/api/dominios`;
  }

  // Headers padr√£o para requisi√ß√µes autenticadas
  getHeaders() {
    const token = authAPI.getToken();
    return {
      'Content-Type': 'application/json',
      ...(token && { 'Authorization': `Bearer ${token}` })
    };
  }

  // Fazer requisi√ß√£o com tratamento de erro
  async makeRequest(endpoint, options = {}) {
    try {
      const url = `${this.baseUrl}${endpoint}`;
      console.log(`üîç Fazendo requisi√ß√£o para: ${url}`);

      const response = await fetch(url, {
        headers: this.getHeaders(),
        ...options
      });

      if (!response.ok) {
        if (response.status === 401) {
          // Token inv√°lido - limpar autentica√ß√£o
          authAPI.logout();
          throw new Error('Token de autentica√ß√£o inv√°lido ou expirado');
        }
        if (response.status === 403) {
          throw new Error('Acesso negado. Apenas administradores podem gerenciar dom√≠nios');
        }
        if (response.status === 404) {
          throw new Error('Dom√≠nio n√£o encontrado');
        }
        if (response.status === 409) {
          throw new Error('Dom√≠nio j√° existe');
        }
        throw new Error(`HTTP ${response.status}: ${response.statusText}`);
      }

      const data = await response.json();
      
      if (!data.success) {
        throw new Error(data.error || data.message || 'Erro desconhecido da API');
      }

      console.log(`‚úÖ Dados recebidos:`, data);
      return data;

    } catch (error) {
      console.error(`‚ùå Erro na requisi√ß√£o ${endpoint}:`, error);
      throw error;
    }
  }

  // Listar todos os dom√≠nios (p√∫blico)
  async listarDominios(options = {}) {
    const { 
      ativo = true, 
      busca = '', 
      limit = 50,
      isRSSapp = undefined 
    } = options;

    const params = new URLSearchParams();
    if (ativo !== undefined) params.append('ativo', ativo.toString());
    if (busca) params.append('busca', busca);
    if (limit) params.append('limit', limit.toString());
    if (isRSSapp !== undefined) params.append('isRSSapp', isRSSapp.toString());

    const endpoint = `?${params.toString()}`;

    try {
      console.log('üìã Listando dom√≠nios...', { ativo, busca, limit, isRSSapp });
      const response = await this.makeRequest(endpoint);
      return response.data || [];
    } catch (error) {
      console.error('‚ùå Erro ao listar dom√≠nios:', error);
      throw error;
    }
  }

  // Obter dom√≠nio espec√≠fico por ID (admin)
  async obterDominio(id) {
    try {
      console.log(`üîç Obtendo dom√≠nio ID: ${id}`);
      const response = await this.makeRequest(`/${id}`);
      return response.data;
    } catch (error) {
      console.error(`‚ùå Erro ao obter dom√≠nio ${id}:`, error);
      throw error;
    }
  }

  // Validar formato de dom√≠nio ou URL (helper)
  validarDominioOuURL(dominio, isRSSapp = false) {
    if (!dominio || typeof dominio !== 'string') {
      return false;
    }

    const dominioTrimmed = dominio.trim();
    
    // Se for RSS.app, deve conter o padr√£o espec√≠fico
    if (isRSSapp) {
      return dominioTrimmed.includes('rss.app/feeds/') && dominioTrimmed.includes('.xml');
    }
    
    // Aceitar URLs completas (http:// ou https://)
    const urlRegex = /^https?:\/\/[a-zA-Z0-9-]+(\.[a-zA-Z0-9-]+)*(\.[a-zA-Z]{2,})(\/.*)?$/;
    
    // Aceitar dom√≠nios simples (exemplo.com, exemplo.com.br)
    const dominioRegex = /^([a-zA-Z0-9-]+\.)+[a-zA-Z]{2,}$/;
    
    return urlRegex.test(dominioTrimmed) || dominioRegex.test(dominioTrimmed);
  }

  // Criar novo dom√≠nio (admin)
  async criarDominio(dominioData) {
    const { nome, dominio, ativo = true, ordem, cor, isRSSapp = false } = dominioData;

    // Valida√ß√µes b√°sicas no frontend
    if (!nome || !dominio) {
      throw new Error('Nome e dom√≠nio s√£o obrigat√≥rios');
    }

    // Validar formato do dom√≠nio baseado no tipo
    if (!this.validarDominioOuURL(dominio, isRSSapp)) {
      if (isRSSapp) {
        throw new Error('URLs do RSS.app devem conter "rss.app/feeds/" e terminar com ".xml"');
      } else {
        throw new Error('Formato inv√°lido. Use uma URL completa (https://exemplo.com/path) ou dom√≠nio simples (exemplo.com.br)');
      }
    }

    try {
      console.log('‚ûï Criando novo dom√≠nio:', { nome, dominio, isRSSapp });
      
      const response = await this.makeRequest('', {
        method: 'POST',
        body: JSON.stringify({
          nome: nome.trim(),
          dominio: dominio.trim().toLowerCase(),
          ativo,
          ordem: ordem ? parseInt(ordem) : null,
          cor: cor || '#0066CC',
          isRSSapp
        })
      });

      return response.data;
    } catch (error) {
      console.error('‚ùå Erro ao criar dom√≠nio:', error);
      throw error;
    }
  }

  // Atualizar dom√≠nio (admin)
  async atualizarDominio(id, dominioData) {
    const { nome, dominio, ativo, ordem, cor, isRSSapp } = dominioData;

    // Validar formato do dom√≠nio se foi fornecido
    if (dominio && !this.validarDominioOuURL(dominio, isRSSapp)) {
      if (isRSSapp) {
        throw new Error('URLs do RSS.app devem conter "rss.app/feeds/" e terminar com ".xml"');
      } else {
        throw new Error('Formato inv√°lido. Use uma URL completa (https://exemplo.com/path) ou dom√≠nio simples (exemplo.com.br)');
      }
    }

    try {
      console.log(`üìù Atualizando dom√≠nio ID: ${id}`, dominioData);
      
      const updateData = {};
      if (nome !== undefined) updateData.nome = nome.trim();
      if (dominio !== undefined) updateData.dominio = dominio.trim().toLowerCase();
      if (ativo !== undefined) updateData.ativo = ativo;
      if (ordem !== undefined) updateData.ordem = ordem ? parseInt(ordem) : null;
      if (cor !== undefined) updateData.cor = cor;
      if (isRSSapp !== undefined) updateData.isRSSapp = isRSSapp;

      const response = await this.makeRequest(`/${id}`, {
        method: 'PUT',
        body: JSON.stringify(updateData)
      });

      return response.data;
    } catch (error) {
      console.error(`‚ùå Erro ao atualizar dom√≠nio ${id}:`, error);
      throw error;
    }
  }

  // Deletar dom√≠nio (admin)
  async deletarDominio(id) {
    try {
      console.log(`üóëÔ∏è Deletando dom√≠nio ID: ${id}`);
      
      const response = await this.makeRequest(`/${id}`, {
        method: 'DELETE'
      });

      return response;
    } catch (error) {
      console.error(`‚ùå Erro ao deletar dom√≠nio ${id}:`, error);
      throw error;
    }
  }

  // Verificar funcionamento do dom√≠nio (admin)
  async verificarDominio(id) {
    try {
      console.log(`üîç Verificando dom√≠nio ID: ${id}`);
      
      const response = await this.makeRequest(`/${id}/verificar`, {
        method: 'POST'
      });

      return response.data;
    } catch (error) {
      console.error(`‚ùå Erro ao verificar dom√≠nio ${id}:`, error);
      throw error;
    }
  }

  // Importar dom√≠nios em lote (admin)
  async importarDominios(dominios) {
    if (!Array.isArray(dominios) || dominios.length === 0) {
      throw new Error('Lista de dom√≠nios deve ser um array n√£o vazio');
    }

    // Validar cada dom√≠nio antes de enviar
    const dominiosValidados = dominios.map((dominio, index) => {
      if (!dominio.nome || !dominio.dominio) {
        throw new Error(`Dom√≠nio ${index + 1}: Nome e dom√≠nio s√£o obrigat√≥rios`);
      }

      const isRSSapp = dominio.isRSSapp || false;

      if (!this.validarDominioOuURL(dominio.dominio, isRSSapp)) {
        if (isRSSapp) {
          throw new Error(`Dom√≠nio ${index + 1}: URLs do RSS.app devem conter "rss.app/feeds/" e terminar com ".xml" (${dominio.dominio})`);
        } else {
          throw new Error(`Dom√≠nio ${index + 1}: Formato inv√°lido (${dominio.dominio}). Use uma URL completa ou dom√≠nio simples`);
        }
      }

      return {
        nome: dominio.nome.trim(),
        dominio: dominio.dominio.trim().toLowerCase(),
        ativo: dominio.ativo !== undefined ? dominio.ativo : true,
        ordem: dominio.ordem ? parseInt(dominio.ordem) : null,
        cor: dominio.cor || '#0066CC',
        isRSSapp
      };
    });

    try {
      console.log(`üì¶ Importando ${dominiosValidados.length} dom√≠nios...`);
      
      const response = await this.makeRequest('/importar', {
        method: 'POST',
        body: JSON.stringify({ dominios: dominiosValidados })
      });

      return response.data;
    } catch (error) {
      console.error('‚ùå Erro ao importar dom√≠nios:', error);
      throw error;
    }
  }

  // Buscar dom√≠nios com filtro
  async buscarDominios(termo, isRSSapp = undefined) {
    try {
      console.log(`üîç Buscando dom√≠nios: ${termo}`);
      return await this.listarDominios({ busca: termo, ativo: undefined, isRSSapp });
    } catch (error) {
      console.error('‚ùå Erro na busca de dom√≠nios:', error);
      throw error;
    }
  }

  // Obter dom√≠nios ativos apenas (para sugest√µes)
  async obterDominiosAtivos(limit = 20, isRSSapp = undefined) {
    try {
      console.log('üìã Obtendo dom√≠nios ativos para sugest√µes...');
      return await this.listarDominios({ ativo: true, limit, isRSSapp });
    } catch (error) {
      console.error('‚ùå Erro ao obter dom√≠nios ativos:', error);
      throw error;
    }
  }

  // Obter apenas sites de not√≠cias ativos
  async obterSitesNoticiasAtivos(limit = 20) {
    try {
      console.log('üì∞ Obtendo sites de not√≠cias ativos...');
      return await this.listarDominios({ ativo: true, limit, isRSSapp: false });
    } catch (error) {
      console.error('‚ùå Erro ao obter sites de not√≠cias:', error);
      throw error;
    }
  }

  // Obter apenas feeds RSS.app ativos
  async obterFeedsRSSappAtivos(limit = 20) {
    try {
      console.log('üì° Obtendo feeds RSS.app ativos...');
      return await this.listarDominios({ ativo: true, limit, isRSSapp: true });
    } catch (error) {
      console.error('‚ùå Erro ao obter feeds RSS.app:', error);
      throw error;
    }
  }

  // Validar se usu√°rio √© admin (helper)
  validarPermissaoAdmin() {
    if (!authAPI.isAuthenticated()) {
      throw new Error('Usu√°rio n√£o est√° autenticado');
    }

    if (!authAPI.isAdmin()) {
      throw new Error('Apenas administradores podem gerenciar dom√≠nios');
    }

    return true;
  }

  // Opera√ß√µes que requerem admin
  async operacaoAdmin(operacao) {
    try {
      this.validarPermissaoAdmin();
      return await operacao();
    } catch (error) {
      if (error.message.includes('admin') || error.message.includes('autenti')) {
        throw error;
      }
      throw new Error(`Erro na opera√ß√£o administrativa: ${error.message}`);
    }
  }

  // Wrapper para opera√ß√µes de admin
  async criarComoAdmin(dominioData) {
    return this.operacaoAdmin(() => this.criarDominio(dominioData));
  }

  async atualizarComoAdmin(id, dominioData) {
    return this.operacaoAdmin(() => this.atualizarDominio(id, dominioData));
  }

  async deletarComoAdmin(id) {
    return this.operacaoAdmin(() => this.deletarDominio(id));
  }

  async verificarComoAdmin(id) {
    return this.operacaoAdmin(() => this.verificarDominio(id));
  }

  async importarComoAdmin(dominios) {
    return this.operacaoAdmin(() => this.importarDominios(dominios));
  }
}

export const dominioService = new DominioService();
export default dominioService;